def get_format(format_id: str) -> ArtifactFormat | None:
    """Return the ArtifactFormat for *format_id*, or None if unknown."""
    return ARTIFACT_FORMATS.get(format_id)


def list_formats(category: ArtifactCategory | None = None) -> list[ArtifactFormat]:
    """Return all formats, optionally filtered by category, in canonical order."""
    formats = list(ARTIFACT_FORMATS.values())
    if category:
        formats = [f for f in formats if f.category == category]
    return formats


def formats_by_category() -> dict[ArtifactCategory, list[ArtifactFormat]]:
    """Return formats grouped by category, in CATEGORY_ORDER order."""
    result: dict[ArtifactCategory, list[ArtifactFormat]] = {c: [] for c in CATEGORY_ORDER}
    for fmt in ARTIFACT_FORMATS.values():
        result[fmt.category].append(fmt)
    return result


