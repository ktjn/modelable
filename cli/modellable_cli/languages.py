"""Artifact format and language registry for Modellable code generation targets."""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Literal

ArtifactCategory = Literal[
    "api_spec", "typed_language", "serialization", "sql_ddl", "data_tool"
]

# All 16 Modellable field types, in canonical order.
MODELLABLE_TYPES = [
    "string", "boolean", "integer", "decimal", "float",
    "timestamp", "date", "time", "duration", "uuid", "binary",
    "enum", "array", "object", "map", "reference",
]


@dataclass
class ArtifactFormat:
    id: str
    name: str
